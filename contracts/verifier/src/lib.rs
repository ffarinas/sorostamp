// Sealed attestations are public goods — keep them alive ~90 days, bump on write.
const DAY_LEDGERS: u32 = 17_280; // ~5s/ledger
const ATT_TTL: u32 = DAY_LEDGERS * 90;
const ATT_TTL_THRESHOLD: u32 = DAY_LEDGERS * 80;

// BN254 scalar-field (Fr) modulus, big-endian. Public signals MUST be < r; otherwise
// x and x+r reduce to the same scalar but are different bytes — a malleability vector
// that would let the same email slip past the nullifier anti-replay.
pub(crate) const FR_MODULUS: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29, 0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x28, 0x33, 0xe8, 0x48, 0x79, 0xb9, 0x70, 0x91, 0x43, 0xe1, 0xf5, 0x93, 0xf0, 0x00, 0x00, 0x01,
];

// Domain-separation tag + version for the statement commitment (prevents cross-context
// / cross-version hash collisions). Bump the version when the signal layout changes.
const DOMAIN_TAG: &[u8] = b"LACRE_ATTESTATION_V1";

fn vk(env: &Env) -> VerificationKey {
    let alpha = Bn254G1Affine::from_array(env, &VK_ALPHA);
    let beta = Bn254G2Affine::from_array(env, &VK_BETA);
    let gamma = Bn254G2Affine::from_array(env, &VK_GAMMA);
    let delta = Bn254G2Affine::from_array(env, &VK_DELTA);

    let mut ic = Vec::new(env);
    for p in VK_IC.iter() {
        ic.push_back(Bn254G1Affine::from_array(env, p));
    }

    VerificationKey {
        alpha,
        beta,
        gamma,
        delta,
        ic,
    }
}

/// Decode a 32-byte big-endian field element into a BN254 scalar.
fn fr_from_bytes(env: &Env, b: &BytesN<32>) -> Bn254Fr {
    let u = U256::from_be_bytes(env, &Bytes::from_array(env, &b.to_array()));
    Bn254Fr::from_u256(u)
}

/// Reject any public signal that isn't a canonical field element (>= r). Without this,
/// `x` and `x + r` reduce to the same scalar (so the proof still verifies) yet hash and
/// key differently — letting the same email slip past anti-replay.
fn ensure_canonical(env: &Env, pub_signals: &Vec<BytesN<32>>) -> Result<(), Error> {
    let modulus = U256::from_be_bytes(env, &Bytes::from_array(env, &FR_MODULUS));
    for s in pub_signals.iter() {
        let u = U256::from_be_bytes(env, &Bytes::from_array(env, &s.to_array()));
        if u >= modulus {
            return Err(Error::FieldElementNotCanonical);
        }
    }
    Ok(())
}

/// Domain-separated sha256 over the proven STATEMENT — every public signal EXCEPT the
/// nullifier at index 0. Commits to "the fact", not to "this particular email", so the
/// frontend can recompute it from the public metadata it shares in the proof link.
///
/// INVARIANT (satisfied by the production VK): there must be >= 1 statement signal AFTER the
/// nullifier, else this degenerates to sha256(DOMAIN_TAG). The real VK exposes 4 statement
/// signals (the packed subject), and its IC count pins pub_signals.len() == 5, so it holds.
fn hash_statement(env: &Env, pub_signals: &Vec<BytesN<32>>) -> BytesN<32> {
    let mut pre = Bytes::from_slice(env, DOMAIN_TAG);
    for s in pub_signals.iter().skip(1) {
        pre.append(&Bytes::from_array(env, &s.to_array()));
    }
    env.crypto().sha256(&pre).to_bytes()
}

/// The pure Groth16/BN254 pairing check: e(-A,B)·e(alpha,beta)·e(vk_x,gamma)·e(C,delta) == 1,
/// where vk_x = IC[0] + Σ signalᵢ·IC[i+1]. No state, no side effects.
fn groth16_verify(env: &Env, proof: &Proof, pub_signals: &Vec<BytesN<32>>) -> bool {
    let bn = env.crypto().bn254();
    let vk = vk(env);

    let mut vk_x = vk.ic.get(0).unwrap();
    for (s, v) in pub_signals.iter().zip(vk.ic.iter().skip(1)) {
        let fr = fr_from_bytes(env, &s);
        let prod = bn.g1_mul(&v, &fr);
        vk_x = bn.g1_add(&vk_x, &prod);
    }

    let neg_a = -proof.a.clone();
    let vp1 = soroban_sdk::vec![env, neg_a, vk.alpha, vk_x, proof.c.clone()];
    let vp2 = soroban_sdk::vec![env, proof.b.clone(), vk.beta, vk.gamma, vk.delta];

    bn.pairing_check(vp1, vp2)
}

#[contract]
pub struct Groth16VerifierBn254;

#[contractimpl]
impl Groth16VerifierBn254 {
    /// Pure verification: is `proof` valid for `pub_signals` under the embedded VK?
    /// Read-only, changes no state. Useful for clients/tests; the real flow is
    /// `submit_proof`, which also seals the result.
    pub fn verify_proof(
        env: Env,
        proof: Proof,
        pub_signals: Vec<BytesN<32>>,
    ) -> Result<bool, Error> {
        if pub_signals.len() + 1 != vk(&env).ic.len() {
            return Err(Error::MalformedVerifyingKey);
        }
        ensure_canonical(&env, &pub_signals)?;
        Ok(groth16_verify(&env, &proof, &pub_signals))
    }

    /// Verify a proof AND seal it as a public attestation.
    ///
    /// This is the real entry point. Order matters:
    ///   1. the proof must verify — that is what makes `pub_signals` trustworthy
    ///      (a valid proof cryptographically binds them; they are not free input);
    ///   2. the nullifier (pub_signals[0], bound by the circuit to the email's DKIM
    ///      signature) must be unseen — otherwise the SAME email is being replayed;
    ///   3. only then do we persist the attestation and emit an event.
    ///
    /// Returns the attestation id (== the nullifier) used by the public /p/:id page.
    pub fn submit_proof(
        env: Env,
        proof: Proof,
        pub_signals: Vec<BytesN<32>>,
    ) -> Result<BytesN<32>, Error> {
        if pub_signals.len() == 0 {
            return Err(Error::NoPublicSignals);
        }
        if pub_signals.len() + 1 != vk(&env).ic.len() {
            return Err(Error::MalformedVerifyingKey);
        }
        // Canonicalize FIRST: x and x+r are the same scalar (the proof verifies for both)
        // but different bytes, so a non-canonical nullifier would dodge anti-replay.
        ensure_canonical(&env, &pub_signals)?;

        // Anti-replay BEFORE the expensive pairing. A nullifier is public, so we reject a
        // known-sealed one for the cost of a storage read — no griefing the sponsor into
        // paying ~25M CPU to re-verify a proof we'd discard anyway.
        let nullifier = pub_signals.get(0).unwrap();
        let key = DataKey::Att(nullifier.clone());
        if env.storage().persistent().has(&key) {
            return Err(Error::AlreadySealed);
        }

        // Only now do we pay for verification.
        if !groth16_verify(&env, &proof, &pub_signals) {
            return Err(Error::InvalidProof);
        }

        let statement_hash = hash_statement(&env, &pub_signals);
        let att = Attestation {
            nullifier: nullifier.clone(),
            statement_hash: statement_hash.clone(),
            ledger: env.ledger().sequence(),
            timestamp: env.ledger().timestamp(),
        };

        env.storage().persistent().set(&key, &att);
        env.storage()
            .persistent()
            .extend_ttl(&key, ATT_TTL_THRESHOLD, ATT_TTL);

        Sealed {
            nullifier: nullifier.clone(),
            statement_hash,
        }
        .publish(&env);

        Ok(nullifier)
    }

    /// Read a sealed attestation by id (== nullifier). Powers the public /p/:id page
    /// with REAL on-chain state. Returns None if nothing was sealed under that id.
    pub fn get_attestation(env: Env, id: BytesN<32>) -> Option<Attestation> {
        env.storage().persistent().get(&DataKey::Att(id))
    }

    /// Whether an email/proof carrying this nullifier has already been sealed.
    pub fn is_sealed(env: Env, nullifier: BytesN<32>) -> bool {
        env.storage().persistent().has(&DataKey::Att(nullifier))
    }
}
